.DATA
A: .WORD 1,2,3,4,5,6,-7,8,9
B: .WORD 0,0,0
.TEXT
LDR R0,=A @ADDRESS REG OF A
LDR R3,=B @ADDRESS REG OF B
MOV R2,#0 @ITERATOR FOR THE OUTER LOOP
	OUTER:
	MOV R4,#0 @ITERATOR FOR THE OUTER LOOP
	MOV R6,#0 @ROW SUM
		INNER:
		LDR R5,[R0]
		ADD R6,R6,R5   @ACCUMLATING THE SUM IN R6 REG
		STR R6,[R3]    @STORING THE ROWSUM INTO MEMORY
		ADD R0,R0,#4
		ADD R4,R4,#1 
		CMP R4,#3
		Bne INNER 
	STR R6,[R3]    
	ADD R3,R3,#4	
	ADD R2,R2,#1 
	CMP R2,#3
	Bne OUTER
.END